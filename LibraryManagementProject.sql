CREATE DATABASE LibraryManagement 

/*==========================TABLES==========================*/

CREATE TABLE Publisher (
ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
NAME_ VARCHAR(100) NOT NULL,
ADDRESS_ VARCHAR(200) NOT NULL,
PHONE VARCHAR(20)
);

CREATE TABLE Book (
ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
TITLE VARCHAR(200) NOT NULL,
PUBLISHER_ID INT NOT NULL CONSTRAINT FK_PUBLISHER_ID1 FOREIGN KEY REFERENCES Publisher(ID) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE Library_Branch (
ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
NAME_ VARCHAR(100) NOT NULL,
ADDRESS VARCHAR(200) NOT NULL
); 

CREATE TABLE Borrower (
CARDNO INT NOT NULL IDENTITY(100,1) PRIMARY KEY,
NAME_ VARCHAR(100) NOT NULL,
SURNAME_ VARCHAR(100) NOT NULL,
PHONE VARCHAR(50) NOT NULL,
ADDRESS_ VARCHAR(200) NOT NULL
);

CREATE TABLE Book_Loans (
ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
BOOKID INT NOT NULL CONSTRAINT FK_Book_ID1 FOREIGN KEY REFERENCES Book(ID) ON UPDATE CASCADE ON DELETE CASCADE,
BRANCHID INT NOT NULL CONSTRAINT FK_Branch_ID1 FOREIGN KEY REFERENCES Library_Branch(ID) ON UPDATE CASCADE ON DELETE CASCADE,
CARDNO INT NOT NULL CONSTRAINT FK_Card_No1 FOREIGN KEY REFERENCES Borrower(CARDNO) ON UPDATE CASCADE ON DELETE CASCADE,
DATEOUT VARCHAR(50) NOT NULL,
DUEDATE VARCHAR(50) NOT NULL
);
/* Use GETDATE() to retrieve the date values for Date out. Use DATEADD for the DueDate */

CREATE TABLE Book_Copies (
ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
BOOKID INT NOT NULL CONSTRAINT FK_Book_ID2 FOREIGN KEY REFERENCES Book(ID) ON UPDATE CASCADE ON DELETE CASCADE,
BRANCHID INT NOT NULL CONSTRAINT FK_Branch_ID2 FOREIGN KEY REFERENCES Library_Branch(ID) ON UPDATE CASCADE ON DELETE CASCADE,
NO_OF_COPIES INT NOT NULL
);

CREATE TABLE Book_Authors (
ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
NAME_SURNAME VARCHAR(100) NOT NULL,
BOOKID INT NOT NULL CONSTRAINT FK_Book_ID3 FOREIGN KEY REFERENCES Book(ID) ON UPDATE CASCADE ON DELETE CASCADE
);

